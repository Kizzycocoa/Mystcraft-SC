    private static void register(String key, Integer... components) {
        PageWordRegistry.register(PageWord.of(
                key,
                "word.mystcraft-sc." + key.toLowerCase(java.util.Locale.ROOT),
                components
        ));
    }

    private static void registerAlias(String alias, String target) {
        PageWord targetWord = PageWordRegistry.get(target);
        if (targetWord == null) {
            throw new IllegalStateException("Attempted to alias page word before target was registered: " + alias + " -> " + target);
        }

        PageWordRegistry.register(new PageWord(
                alias,
                "word.mystcraft-sc." + alias.toLowerCase(java.util.Locale.ROOT),
                targetWord.componentIndices(),
                targetWord.colors()
        ));
    }
